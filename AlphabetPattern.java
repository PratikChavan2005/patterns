class AlphabetPattern{
public static void p14(){

                for(char  i='A';i<='E';i++){
                    for(char j = 'A';j<=i;j++ ){
                        System.out.print(j);
                    }
                    System.out.println();
                }
                 

        }
    
    public static void main(String[] args){
         
            p14();
    }
}